# 연산 우선 순위

# 1. 문법과 개념 설명
# 파이썬의 연산자는 다음과 같은 우선 순위를 갖습니다 (줄이 내려갈수록 우선순위가 낮아집니다):
# - 괄호: ()
# - 지수: **
# - 부호: +x, -x
# - 곱셈/나눗셈: *, /, %, //
# - 덧셈/뺄셈: +, -

# 2. 어떻게 실수할 수 있는지

# 실수 예시 1: 연산 우선 순위를 간과하고 연산을 수행
result = 1 + 3 * 2  # 이 연산은 1 + (3 * 2)로 해석되어, 7이 아닌 7이 됩니다.
print(result)  # 7이 출력됩니다.

# 실수 예시 2: 복잡한 연산에서 괄호를 사용하지 않아 잘못된 결과를 얻는 경우
result2 = 3 + 2 * 2 - 4 / 2  # 이 연산은 3 + (2 * 2) - (4 / 2)로 해석되어, 5가 아닌 5.0이 됩니다.
print(result2)  # 5.0이 출력됩니다.

# 3. 올바른 사용법

# 올바른 사용법 예시 1: 연산의 우선 순위를 명확하게 하기 위해 괄호 사용
correct_result1 = (1 + 3) * 2  # 괄호를 사용하여 덧셈을 먼저 수행하도록 지정합니다.
print(correct_result1)  # 8이 출력됩니다.

# 올바른 사용법 예시 2: 복잡한 연산에서 괄호를 사용하여 우선 순위를 명확하게 지정
correct_result2 = (3 + 2) * (2 - 4 / 2)  # 괄호를 사용하여 연산의 우선 순위를 명확하게 지정합니다.
print(correct_result2)  # 5.0이 출력됩니다.

# 일반적인 팁: 연산의 우선 순위에 혼란이 올 경우, 괄호를 사용하여 우선 순위를 명확하게 지정하는 것이 좋습니다.
